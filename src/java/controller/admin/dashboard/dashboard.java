package controller.admin.dashboard;

import jakarta.servlet.ServletContext;
import java.io.IOException;
import java.io.PrintWriter;
import jakarta.servlet.ServletException;
import jakarta.servlet.http.HttpServlet;
import jakarta.servlet.http.HttpServletRequest;
import jakarta.servlet.http.HttpServletResponse;
import java.util.List;
import model.queryEvent;
import model.queryReportEvent;
import model.queryUser;
import schema.Event;
import schema.ReportEvent;

/**
 *
 * @author LENOVO
 */
public class dashboard extends HttpServlet {

    /**
     * Processes requests for both HTTP <code>GET</code> and <code>POST</code>
     * methods.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest req, HttpServletResponse res)
            throws ServletException, IOException {
        // get all report by id account
        ServletContext servletContext = getServletContext();
        String token = (String) servletContext.getAttribute("token"); 
        queryUser qUser = queryUser.createQueryUSer();// get account id
        
        queryReportEvent qReportEvent = queryReportEvent.createInstanceReportEvent();
        List<ReportEvent> listReport = qReportEvent.listReportByUserId(qUser.getIdByToken(token));
        req.setAttribute("report", listReport);
        //end
        req.getRequestDispatcher("/views/admin/pages/Dashboard/dashboard.jsp").forward(req, res);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    /**
     * Returns a short description of the servlet.
     *
     * @return a String containing servlet description
     */
    @Override
    public String getServletInfo() {
        return "Short description";
    }// </editor-fold>

}
