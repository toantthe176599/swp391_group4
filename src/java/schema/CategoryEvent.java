package schema;

/**
 *
 * @author LENOVO
 */
public class CategoryEvent {

    private String id, description;

    public CategoryEvent() {
    }

    public CategoryEvent(String id, String description) {
        this.id = id;
        this.description = description;
    }

    public String getId() {
        return id;
    }

    public String getDescription() {
        return description;
    }

}
